// Switch-Case

const option = 1;
switch (option) {
  case 1:
    console.log("Namaste!");
    break;
  case 2:
    1;
    console.log("Hello");
    break;
  case 3:
    console.log("Hola");
    break;
  default:
    console.log("Invalid Option.");
}

// For Loop
for (let i = 1; i <= 10; i = i + 1) {
  console.log("Rabindra" + i);
}
